# convert tuple to list
result = list((1, 2, 3))   # [1, 2, 3]
print(result)

# convert string to list
result = list("Hello")   # ['H', 'e', 'l', 'l', 'o']
print(result)

# convert dictionary to list
result = list({2: 4, 10: 20})   # [2, 10]
print(result)

# convert set to list
result = list({1, 2, 3})   # [1, 2, 3]
print(result)
